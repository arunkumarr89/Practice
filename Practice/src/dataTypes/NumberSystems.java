package dataTypes;

public class NumberSystems {
	public static void main(String[] args) {
		//Java Supports
		/*
		 * Arabic(base-10),, Octal(base-8),, Hexadecimal(base-16), binary()base-2)
		 */
		
		int a=100;//arabic
		int b=0144;//octal - starts with zero
		int c=0x0064;// Hexadecimal - small X
		int d=0X0064;//Hexadecimal - capital X
		int e=0b1100100;//binary small b
		int f=0B1100100;//binary capotal B
		//Underscores syntax are applicable for all
	}

}
